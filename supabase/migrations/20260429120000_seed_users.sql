-- Insert deduplicated rows into users table
insert into public.users (email, full_name, phone_number, selected_hostel, role, base44_imported, created_at)
select distinct on (email)
  email, full_name, phone_number, selected_hostel, role, true, created_at::timestamptz
from tmp_users
on conflict (email) do update set
  full_name        = excluded.full_name,
  phone_number     = case when excluded.phone_number != '' then excluded.phone_number else public.users.phone_number end,
  selected_hostel  = case when excluded.selected_hostel != '' then excluded.selected_hostel else public.users.selected_hostel end,
  role             = excluded.role,
  base44_imported  = true;
